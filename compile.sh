#!/bin/bash

# 编译脚本

flag=""

for arg in $*;
do
    if [[ ${arg} =~ "verbose" ]]
    then
        flag+=" VERBOSE=1"
    fi
done

[ -d "build" ] || mkdir build

# 编译命令
cd build && cmake .. && make -j4 ${flag}

if [[ ${flag} == "" ]]
then
    echo "./compile [argv]
        argv可取:
            verbose:打印详细信息
    "
fi


